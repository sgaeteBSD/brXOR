def compHam(h1, h2):
    inc = 0
    for i in range(len(h1)):
        xor = h1[i] ^ h2[i]
        while xor:
            inc += xor & 1
            xor >>= 1
    return inc
            
def B64ToBytes(b64):
    byts = bytearray()
    i = 0
    while i < len(b64):
        chunk = b64[i:i+4]
        i += 4
        
        while len(chunk) < 4:
            chunk += '='
        
        bits = 0
        chars = 0
        
        for j in range(4):
            c = chunk[j]
            if c == '=':
                val = 0
            else:
                chars += 1
                for k in range(64):
                    if (BASE64[k] == c):
                        val = k
                        break
            bits |= val << (18 - 6 * j)
        for j in range (chars - 1):
            byte = (bits >> (16 - 8 * j)) & 0xFF
            byts.append(byte)
    return byts
    
def hexToBytes(hx):
    return list(bytes.fromhex(hx))

def bytesToHex(byts):
    return (bytes(byts)).hex()
    
def bytesToASCII(byts):
    return bytes(byts).decode("ascii", errors="ignore")

def ASCIIToBytes(stri):
    return stri.encode("ascii", errors="ignore")

def repeatXOR(data, *keys):
    z = 0
    listed = []
    for char in data:
        key = keys[z % len(keys)]
        listed.append(char ^ key)
        z += 1
    return bytes(listed)

def scoreConvert(byts):
    highScore = 0
    bestResult = []
    bestKey = None
    
    for char in CHARS:
        key = ord(char)
        xored = singleXOR(byts, key)
        decoded = bytesToASCII(xored)
    
        score = 0
        for c in decoded:
            if c.islower():
                score += 2
            elif c.isupper():
                score += 1
            elif c == ' ':
                score += 1
            elif c in ".,!?":
                score += 0.5
            elif c.isdigit():
                score -= 1
            else:
                score -= 0.5
        if score > highScore:
            highScore = score
            bestResult = xored
            bestKey = key
    return bestKey, bestResult, highScore 

def singleXOR(data, key):
    return bytes([b ^ key for b in data])

